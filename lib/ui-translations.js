export function translateUiString(locale, key, variables = {}) {
  const normalizedLocale = normalizeLocale(locale);
  const value =
    UI_TRANSLATIONS[normalizedLocale]?.[key] ??
    UI_TRANSLATIONS[DEFAULT_LOCALE]?.[key] ??
    key;

  return interpolateTemplate(decodeMojibakeText(value), variables);
}

export function hasUiTranslation(locale, key) {
  return Object.prototype.hasOwnProperty.call(UI_TRANSLATIONS[normalizeLocale(locale)] || {}, key);
}

export function getUiTranslations(locale) {
  return UI_TRANSLATIONS[normalizeLocale(locale)] || UI_TRANSLATIONS[DEFAULT_LOCALE];
}
